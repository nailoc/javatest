package com.hk.app;

public class Exam088 {

	public static void main(String[] args) {

		// create int variables
	    int num1 = 80;
	    int num2 = 81;

	    // convert int to char
	    // typecasting
	    char a = (char)num1;
	    char b = (char)num2;

	    // print value
	    System.out.println(a);    // P
	    System.out.println(b);    // Q
	    
	    
	    ///////////////////////////////////
	    
	    // create int variables
	    int num11 = 1;
	    int num22 = 9;

	    // convert int to char
	    char aa = (char)(num11 + '0');
	    char bb = (char)(num22 + '0');

	    // print value
	    System.out.println(aa);    // 1
	    System.out.println(bb);    // 9
	}

}
